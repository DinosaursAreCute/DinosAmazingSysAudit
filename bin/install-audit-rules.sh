#!/usr/bin/env bash
# shellcheck disable=SC1091
#
# Installs the auditd rules auditsys depends on:
#   - file watches (-w) on configured paths, tagged -k auditsys
#   - an execve rule keyed on loginuid (auid) so `docker ...` run via
#     sudo/su is attributed back to the real logged-in user
#
# Deliberately a thin bash script: this is exactly the kind of one-shot,
# system-level, root-only job bash is good at. Everything else (parsing,
# storage, reporting) lives in the Python package.
set -euo pipefail

AUDIT_KEY="auditsys"
RULES_FILE="/etc/audit/rules.d/auditsys.rules"
DRY_RUN=0
UNINSTALL=0

usage() {
    cat <<EOF
Usage: $0 [--dry-run] [--uninstall] [--config PATH]

  --dry-run     print the auditctl commands / rules file without applying
  --uninstall   remove auditsys rules (auditctl -D -k auditsys equivalent, targeted)
  --config PATH path to auditsys config (default: auto-detected by auditsys itself)
EOF
}

CONFIG_PATH=""
while [[ $# -gt 0 ]]; do
    case "$1" in
        --dry-run) DRY_RUN=1; shift ;;
        --uninstall) UNINSTALL=1; shift ;;
        --config) CONFIG_PATH="$2"; shift 2 ;;
        -h|--help) usage; exit 0 ;;
        *) echo "unknown arg: $1" >&2; usage; exit 1 ;;
    esac
done

if [[ "$(id -u)" -ne 0 ]]; then
    echo "This must be run as root (auditctl requires it)." >&2
    exit 1
fi

if ! command -v auditctl >/dev/null 2>&1; then
    echo "auditctl not found — install auditd first (see scripts/install.sh)." >&2
    exit 1
fi

if [[ "$UNINSTALL" -eq 1 ]]; then
    echo "Removing auditsys rules..."
    if [[ -f "$RULES_FILE" ]]; then
        [[ "$DRY_RUN" -eq 1 ]] || rm -f "$RULES_FILE"
        echo "removed $RULES_FILE"
    fi
    [[ "$DRY_RUN" -eq 1 ]] || auditctl -D -k "$AUDIT_KEY" 2>/dev/null || true
    [[ "$DRY_RUN" -eq 1 ]] || { command -v augenrules >/dev/null 2>&1 && augenrules --load || true; }
    exit 0
fi

# Pull watched_paths out of the auditsys config via the Python package itself,
# so there is exactly one source of truth for what's watched.
if ! command -v python3 >/dev/null 2>&1; then
    echo "python3 not found." >&2
    exit 1
fi

mapfile -t WATCHED_PATHS < <(
    AUDITSYS_CONFIG="${CONFIG_PATH:-}" python3 - <<'PY'
import os
import sys
sys.path.insert(0, os.environ.get("AUDITSYS_PYTHONPATH", ""))
try:
    from auditsys import config as cfgmod
except ImportError:
    print("", file=sys.stderr)
    sys.exit(0)
cfg = cfgmod.load_config()
for p in cfg.get("watched_paths", []):
    print(p)
PY
)

if [[ ${#WATCHED_PATHS[@]} -eq 0 ]]; then
    echo "No watched_paths resolved from config; falling back to built-in defaults." >&2
    WATCHED_PATHS=(/etc /usr/bin/docker /usr/bin/docker-compose /run/docker.sock /var/lib/docker)
fi

{
    echo "## Generated by install-audit-rules.sh — do not hand-edit, re-run the script instead."
    for p in "${WATCHED_PATHS[@]}"; do
        [[ -e "$p" ]] || { echo "## skipped (not present on this host): $p" ; continue; }
        echo "-w $p -p wa -k ${AUDIT_KEY}"
    done
    # loginuid-attributed execve watch for docker/containerd/runc binaries —
    # this is the rule that solves "who ran the command that nuked the stack".
    for bin in /usr/bin/docker /usr/bin/docker-compose /usr/bin/containerd /usr/sbin/runc; do
        [[ -e "$bin" ]] || continue
        echo "-a always,exit -F arch=b64 -F path=${bin} -F perm=x -F auid>=1000 -F auid!=4294967295 -k ${AUDIT_KEY}"
        echo "-a always,exit -F arch=b32 -F path=${bin} -F perm=x -F auid>=1000 -F auid!=4294967295 -k ${AUDIT_KEY}"
    done
} > /tmp/auditsys.rules.$$

if [[ "$DRY_RUN" -eq 1 ]]; then
    echo "--- would write ${RULES_FILE} ---"
    cat /tmp/auditsys.rules.$$
    rm -f /tmp/auditsys.rules.$$
    exit 0
fi

install -m 0640 /tmp/auditsys.rules.$$ "$RULES_FILE"
rm -f /tmp/auditsys.rules.$$
echo "wrote $RULES_FILE"

if command -v augenrules >/dev/null 2>&1; then
    augenrules --load
elif command -v auditctl >/dev/null 2>&1; then
    auditctl -R "$RULES_FILE"
fi
echo "auditsys audit rules installed and loaded."
auditctl -l | grep "$AUDIT_KEY" || echo "warning: no rules matching key '$AUDIT_KEY' found after load"
